      <b><blockquote>Judging Criteria</blockquote></b>

  1. A robot is declared victorious if its opponent is immobilized.<br>
2.A robot will be declared immobile if it cannot display linear motion of at least one inch in a timed period of 30 seconds. A bot with one side of its drive train disabled will not be counted out if it can demonstrate some degree of controlled movement. In case both the robots remain mobile after the end of the round then the winner will be decided subjectively.<br>
3.A robot that is deemed unsafe by the judges after the match has begun will be disqualified and therefore declared the loser. The match will be immediately halted and the opponent will be awarded a win.<br>
4.If a robot is thrown out of the arena the match will stop immediately, and the robot still inside the arena will automatically be declared as the winner.<br>
5.Robots cannot win by pinning or lifting their opponents. Organizers will allow pinning or lifting for a maximum of 10 seconds per pin/lift then the attacker robot will be instructed to release the opponent. If, after being instructed to do so, the attacker is able to release but does not, their robot may be disqualified. If robots become entangled or a crushing or gripping weapon is employed and becomes trapped within another robot, then the competitors should make the timekeeper aware, the fight should be stopped and the robots separated by the safest means.<br>
Points will be given on the basis of aggression, damage, control and strategy. <br>
<b>Aggression –</b> Aggression is judged by the frequency, severity, boldness and effectiveness of attacks deliberately initiated by the robot against its opponent. If a robot appears to have accidentally attacked an opponent, that act will not be considered Aggression. <br>
<b>Control –</b> Control means a robot is able to attack an opponent at its weakest point, use its weapons in the most effective way, and minimize the damage caused by the opponent or its weapons. <br>
<b>Damage – </b>Through deliberate action, a robot either directly or indirectly reduces the functionality, effectiveness or defensibility of an opponent. Damage is not considered relevant if a robot inadvertently harms itself. Also, if a pressure vessel or a rapidly spinning device on a robot fragments, any damage to the opponent will not be considered "deliberate". <br>
<b>Strategy – </b>The robot exhibits a combat plan that exploits the robot's strengths against the weaknesses of its opponent. Strategy is also defined as a robot exhibiting a deliberate defense plan that guards its weaknesses against the strengths of the opponent. <br><br>

<b>NOTE:</b> Qualification of a robot to next level is subjective and totally on the decision of the judges. A robot winning in a round against an opponent doesn’t guarantee its entrance into the next round. If the judges find the winner robot incompetent to enter into the next round, it may get disqualified. Judges can disqualify both the robots of a match from advancing to the next round. All the decisions taken by the judge will be final and binding to all. Any queries afterwards will not be entertained.
     
