       <b><blockquote>Dimensions and Fabrications</blockquote></b>
1.The machine should fit in a box of dimension 750mm x 750mm x 1000mm (l x b x h) at any given point during the match. The external device used to control the machine or any external tank is not included in the size constraint.<br>
2.The machine should not exceed 60 kg of weight including the weight of pneumatic source or tank. All pneumatic tanks/source and batteries being considered. Weight of remote controller will not be counted.<br>
3.The machine should not intentionally be split into components or any parts should not be intentionally detached from machine at any point of match.<br>
<b><blockquote> General Construction</blockquote></b>
1.Any on-board equipment that could require attention between duels for maintenance - e.g. recharging of compressed gas cylinders, charging batteries, resetting of weapons, etc. - should be easily and quickly accessible i.e. systems must be installed in such a manner that they can be removed for filling and testing within five minutes.
<b><blockquote>Style and Mobility</blockquote></b>
1.All robots must have easily visible and controlled mobility in order to compete. Methods of mobility include:<br>
2.Rolling (wheels, tracks or the whole robot).<br>
3.Non-wheeled robots having no rolling elements in contact with the floor and no continuous rolling or cam operated motion in contact with the floor, either directly or via a linkage. Motion is "continuous" if continuous operation of the drive motor(s) produces continuous motion of the robot. Linear-actuated legs and novel non wheeled drive systems come under this category.<br>
4.Jumping and hopping is not allowed.<br>
5.Flying (using aerofoil, helium balloons, ornithopters, etc.) is not allowed.<br>
<b><blockquote>Robot Control Requirements</blockquote></b>
1.The machine should be controlled through WIRELESS remote. Power supply has to be on-board. Refer below for further details on battery and power.<br>
2.There should be binding capability between transmitters and receivers. The remotes with such facility will only be allowed.<br>
3.The team must have at least four frequency wireless remote control circuit or two dual control circuits which may be interchanged before the start of the race to avoid frequency interference with other teams. The case of any interference in the wireless systems will not be considered for rematch or results.<br>
4.Remote control systems from toys might be used. Remote control systems available in the market may also be used.<br>
5.Non standard or self-made remote control systems must first be approved by the organizers or the judges.<br>
6.Team should pair up the wireless remote with the machine before putting it into the arena.<br>

<b><blockquote>Battery and Power</blockquote></b>
1.The machine can be powered electrically only. Use of an IC engine in any form is not allowed.<br>
2.Power supply has to be ON-BOARD.<br>
3.On board batteries must be sealed, immobilized-electrolyte types (such as gel cells, lithium, NiCad, NiMH, or dry cells).<br>
The electric voltage between 2 points anywhere in the machine should not be more than 36V DC at any point of time. For using transformers as source they should be verified by the organizers and output cannot be greater than 36V DC. (A variance of 2V would be considered due to batteries not being exactly equal to said voltages )<br>
4.All efforts must be made to protect battery terminals from a direct short and causing a battery fire, failure to do so will cause direct disqualification.<br>
5.Use of damaged, non-leak proof batteries may lead to disqualification.<br>
6.Special care should be taken to protect the on-board batteries. If judges found that the battery is not properly protected, then team will be disqualified immediately.<br>
7.Change of battery will not be allowed during the match. (exemption can be made in case of battery damage).<br>
8.There should be a provison for a 'kill switch 'which should be easily accessible to stop the bot in case of any uncontrolled bot response .<br>
9.It is suggested to have extra battery ready and charged up during competition so that on advancing to next level, you don’t have to wait or suffer due to uncharged battery. If teams don’t show up on allotted slot, they will be disqualified.<br>

<b><blockquote>Pneumatics</blockquote></b>
1.Robot can use pressurized non-inflammable gases to actuate pneumatic devices. Maximum allowed outlet nozzle pressure is 10 bar. The storage tank and pressure regulators used by teams need to be certified and teams using pneumatics are required to produce the Safety and Security letters at the Registration Desk at the venue. Failing to do so will lead to direct disqualification.<br>
2.Participants must be able to indicate the used pressure with integrated or temporarily fitted pressure gauge. Also there should be provision to check the cylinder pressure on the bot.<br>
3.The maximum pressure in cylinder should not exceed the rated pressure at any point of time.<br>
4.You must have a safe way of refilling the system and determining the on board pressure.<br>
5.All pneumatic components on board a robot must be securely mounted. Care must be taken while mounting the pressure vessel and armor to ensure that if ruptured it will not escape the robot. The terms 'pressure vessel, bottle, and source tank' are used interchangeably.<br>
6.Entire pneumatic setup should be onboard, no external input (from outside the arena) can be given to the robot for functioning of its pneumatic system.<br>

<b><blockquote>Hydraulics</blockquote></b>
1.Robot can use non-inflammable liquid to actuate hydraulic devices e.g. cylinders.<br>
2.All hydraulic components on-board must be securely mounted. Special care must be taken while mounting pump, accumulator and armor to ensure that if ruptured direct fluid streams will not escape the robot.<br>
3.All hydraulic liquids are required to be non-corrosive and your device should be leak proof. Maximum allowed pressure is 10 bars.<br>
4.Participant must be able to indicate the used pressure with integrated or temporarily fitted pressure gauge.<br>
5.Entire hydraulic setup should be onboard, no external input (from outside the arena) can be given to the robot for functioning of its hydraulic system.<br>

<b><blockquote>Weapons Systems</blockquote></b> Robots can have any kind of magnetic weapons, cutters, flippers, saws, lifting devices, spinning hammers etc. as weapons with following exceptions and limitations: 
1.Liquid projectiles.<br>
2.Any kind of inflammable liquid.<br>
3.Flame-based weapons.<br>
4.Any kind of explosive or intentionally ignited solid or potentially ignitable solid.<br>
5.Nets, tape, glue, or any other entanglement device.<br>
6.High power magnets or electromagnets.<br>
7.Radio jamming, Tasers, tesla coils, or any other high-voltage device.<br>
8.Tethered or un-tethered projectiles.<br>
9.Any kind of spinning weapon or the robot itself should not cause any damage to the arena.<br>

<b><blockquote>Safety Rules </blockquote></b>
Compliance with all event rules is mandatory. It is expected that competitors stay within the rules and procedures of their own accord and do not require constant policing.<br>
1.Special care should be taken to protect the on-board batteries and pneumatics, robot without proper protection will not be allowed to compete.<br>
2.If you have a robot or weapon design that does not fit within the categories set forth in these rules or is in some way ambiguous or borderline, please contact the event organizers. Safe innovation is always encouraged, but surprising the organizers with your brilliant exploitation of a loophole may cause your robot to be disqualified before it even competes.<br>
3.Each event has safety inspections. It is at their sole discretion that your robot is allowed to compete. As a builder you are obligated to disclose all operating principles and potential dangers to the inspection staff.<br>
4.Proper activation and deactivation of robots is critical. Robots must only be activated in the arena, testing areas, or with expressed consent of the event coordinators.<br>
5.All weapons must have a safety cover on any sharp edges.<br>
6.All participants build and operate robots at their own risk. Combat robotics is inherently dangerous. There is no amount of regulation that can encompass all the dangers involved. Please take care to not hurt yourself or others when building, testing and competing. Any kind of activity (repairing, battery handling, pneumatics systems etc.) which may cause damage to the surroundings during the stay of the teams in the competition area should not be carried out without the consent of organizers. <br>7.Not following this rule may result in disqualification.<br>
8.All the resources provided at the time of competition from the organizers should be strictly used only after the consent of the organizers.<br>
9.Once the robots have entered into the arena, no team member can enter into the arena at any point of time. In case if a fight has to be halted in between and some changes have to be done in the arena or condition on the robot(s), it will be done by organizers only.<br>
10.There should be a kill switch (PUSH TYPE) on the bot visible normally which would disconnect all the power supply.<br>
11.A driver must have a helmet which covers his entire face along with other safety materials if any. Each team must bring its own helmet. No helmet will be provided by Technovanza Committee.<br>


