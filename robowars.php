      <b><blockquote>Introduction</blockquote></b>

   <b><i>‘War does not determine who is right. Only who is left’ </i></b>
   <br>

Passionate about Bots? Or you just like to watch things get crushed from fierce competition? The path to glory passes through the alley of destruction, lethal combats, violent bots, and flying blades! Merge your technical know-how with the ferocious gladiator in you! Make your bot fight for the ultimate glory or land up in the junk yard! <br>
So folks, fight smart, be patient and pray. Seriously, pray because robowars just got crazier in its new avatar! <br>
<b><blockquote>Team Specification: </blockquote></b>
Any team can participate in Robowars, Technovanza. A team may consist of a maximum of 6 participants. These participants can be from same or different institutes<br>. 
Team Name: Every team must have a name which must be unique. Technovanza reserves the right to reject entries from any Team whose name it deems inappropriate, offensive or conflicting. Organizers must be notified during if a Team's name has been changed.<br> 

Team Representative: Each team must specify their Team Representative (Leader) at the time of registration on the website. All important communications between Technovanza and the registered teams will be done through their Team Representative. The Team Representatives must submit valid contact details (phone no., email ID etc.) at the time of registration.<br>

<b><blockquote>Certificate Policy: </blockquote> </b>
1. Certificate of appreciation/participation will be awarded to the all teams. And certificate of excellence will be given to top three teams.<br>
2. Disqualified teams will not be considered for any certificates.<br>
      
     
