          <br>
          1.Make a wireless remote controlled machine, powered by only an IC engine, which can race against other opponents on an off-road dirt track with many obstacles.<br>
          2.There will be three different types of races in qualification round:<br>
          <li>Time Trial<br>
          <li>Drag<br>
          <b><blockquote>A.Time trial :</blockquote></b><br>
          Each team will get two trials. Each trial consists of two laps. The fastest trial will be considered!!! Not more than two team members will be allowed in the arena at a time. Checkpoints will be placed throughout the arena. If a machine is stuck or falls off the arena, that team will be allowed to pick it up and place it at a checkpoint behind that point, the time for which will be added to the total lap time. If a machine disintegrates in the arena leaving behind loose parts the team will be disqualified. Once the race begins team members will be immediately disqualified if found touching their’s or opponent’s machine. If any of the bots starts off before the flag is waved, the counter would be restarted and the machines will get a second chance. However, if any machine starts off before the waving of flag (or countdown) for a second time, it will be disqualified. Once disqualified the team can't participate again in the event.If any member of a team is found purposely damaging other team's bot the team will be disqualified.<br>
          <b><blockquote>B.Drag :</blockquote></b>
          Each team will get two laps. The fastest lap will be considered!! There will be no obstacles in this race type. The teams just have to accelerate on a straight path. Once the race begins the bot is not allowed to stop until it crosses the finishing line. If the bot stops in between the race then that lap will not be considered. The bot is not allowed to touch the boundary tires. Extra time will be be added if the bot touches any boundary tires.
          Total= A + B .Top teams in qualifying round will make it to the second round.<br>
                 

