      <div id="rules" class="tab-pane fade in">

      <br>
        1.The machine should fit in a box of dimensions 700mm x 500mm x 600mm throughout the the race, excluding the external device which is used to control the machine.<br>
        2.The teams must design a purely wireless remote controlled machine.<br>
        3.The machine parts can be roughly classified into structural and functional parts: Functional parts – ready-made versions of Gears, differential gear, engine, springs, shock absorbers, servo motors (non propulsion purposes only), batteries, wheels and wheel can be used. Structural parts - Chassis, steering mechanism, shock towers and suspension (excluding the upper suspension arm, suspension spring and shock absorbers) have to be built by the participants themselves.<br>
        4.Judging for the same will be strict and the participant will be immediately disqualified if any of the above structural components are found to be ready-made.<br>
        5.After a thorough inspection, machines which seem dangerous will be disqualified. This decision of the judges and the organizers will be ultimate.<br>
        6.Minimum thickness of tyres is 3 inch. Selection of a good width will ensure better performance in dirt tracks.<br>
        7.Brake Mechanism: It is compulsory to incorporate a braking mechanism in the car. Participants have to fabricate the brake pad as a part of the braking mechanism. Any other part used in braking mechanism (including the brake disk) can be readymade.<br>
        8.Wheel Hub: Any part rigidly attached to the wheel hub will be considered as a part of it and hence can be ready-made. E.g.:- ball stud.<br>
        9.Steering Mechanism: Any part which is connected to steering rod rigidly i.e. has no degrees of freedom with respect to steering rod will be considered as part of steering rod and thus has to be fabricated by participants.<br>
        10.Suspension mechanism: Any part rigidly connected to suspension arms or one with no degrees of freedom with respect to suspension arm will be considered as its part and has to be fabricated by the participants, except the upper suspension arm. For example both the heim joint for the upper suspension arm and the stud rigidly connected to the wheel hub can be bought from the market.<br>
        11.If there are parts used in the concerned joint which are neither rigidly connected with suspension or the hub, steering system or hub; they can be taken ready-made from the market.<br>
        12.The Bot must be propelled by only mechanical power generated by an internal combustion (IC) engine for propulsion. Only one IC engine should be used in the machine. Use of any other sources such as chemicals, compressed gas, rockets etc. is not allowed.<br>
        13.The maximum allowed capacity of IC engine to be used is 4.6 cc (i.e. Participants can also use 2.5 cc, 3 cc, 3.5 cc or any other IC engine lower in capacity).<br>
        14.The electric voltage anywhere in the machine should not exceed 12V at any point of time.<br>
    

      
     
